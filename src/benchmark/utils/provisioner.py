import base64
import json
import os

from .gh_client import GitHubClient


class RepoProvisioner:
    """Handles the physical setup of a GitHub repository for benchmarking."""

    def __init__(self, gh_client: GitHubClient):
        self.gh_client = gh_client

    def provision(self, workflow_path: str, required_files: dict = None):
        """Ensures the repo exists, is initialized, and has the necessary files."""
        # 1. Ensure repo exists and get default branch
        repo_info = self._ensure_repo_exists()
        default_branch = repo_info.get("defaultBranchRef", {}).get("name") or "main"

        # 2. Initialize if empty
        if repo_info.get("isEmpty"):
            print(f"Initializing empty repository {self.gh_client.repo}...")
            self._put_file(
                "README.md",
                "# Benchmark Repository\nGenerated by AI Benchmark Suite.",
                "initial commit",
                default_branch,
            )
            default_branch = "main"  # GitHub defaults to main on first commit usually

        # 3. Sync Workflow
        if os.path.exists(workflow_path):
            with open(workflow_path, "r") as f:
                content = f.read()
            print("Syncing workflow to .github/workflows/workflow.yml...")
            self._put_file(
                ".github/workflows/workflow.yml",
                content,
                "sync workflow",
                default_branch,
            )

        # 4. Sync Scenario Files
        if required_files:
            for repo_path, local_path_or_content in required_files.items():
                content = local_path_or_content
                if os.path.exists(local_path_or_content):
                    with open(local_path_or_content, "r") as f:
                        content = f.read()

                print(f"Syncing scenario file {repo_path}...")
                self._put_file(repo_path, content, f"sync {repo_path}", default_branch)

    def _ensure_repo_exists(self):
        """Checks if repo exists, creates it if not."""
        stdout, stderr = self.gh_client.run_gh(
            ["repo", "view", "--json", "defaultBranchRef,isEmpty"]
        )
        if (
            "repository not found" in stderr.lower()
            or "could not resolve to a repository" in stderr.lower()
        ):
            print(f"Repository {self.gh_client.repo} not found. Creating...")
            self.gh_client.run_gh(
                ["repo", "create", self.gh_client.repo, "--public", "--confirm"]
            )
            stdout, _ = self.gh_client.run_gh(
                ["repo", "view", "--json", "defaultBranchRef,isEmpty"]
            )

        return (
            json.loads(stdout)
            if stdout
            else {"isEmpty": True, "defaultBranchRef": {"name": "main"}}
        )

    def _put_file(self, path: str, content: str, message: str, branch: str):
        """Uploads or updates a file using the GitHub API."""
        encoded_content = base64.b64encode(content.encode()).decode()

        # Check if file exists to get its SHA (required for updates)
        stdout, _ = self.gh_client.run_gh(
            [
                "api",
                f"/repos/{self.gh_client.repo}/contents/{path}",
                "--query",
                f"ref={branch}",
            ]
        )
        sha = None
        if stdout:
            try:
                file_data = json.loads(stdout)
                sha = file_data.get("sha")
            except json.JSONDecodeError:
                pass

        args = [
            "api",
            "--method",
            "PUT",
            f"/repos/{self.gh_client.repo}/contents/{path}",
            "-f",
            f"message={message}",
            "-f",
            f"content={encoded_content}",
            "-f",
            f"branch={branch}",
        ]
        if sha:
            args += ["-f", f"sha={sha}"]

        self.gh_client.run_gh(args)
