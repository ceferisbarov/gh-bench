import os

import click

from .gh_client import GitHubClient


class RepoProvisioner:
    """Handles the physical setup of a GitHub repository for benchmarking."""

    def __init__(self, gh_client: GitHubClient):
        self.gh_client = gh_client

    def provision(
        self,
        workflow_dir: str,
        required_files: dict = None,
        branch: str = None,
        template_repo: str = None,
        secrets: dict = None,
        variables: dict = None,
        substitution_map: dict = None,
    ):
        """Creates a fresh repo (via fork or creation) and sets up files, secrets, and variables."""
        if template_repo:
            click.echo(f"Forking {template_repo} to {self.gh_client.repo_name}...")
            success, err = self.gh_client.fork_repo(template_repo)
        else:
            click.echo(f"Creating repository {self.gh_client.repo_name}...")
            success, err = self.gh_client.create_repo(public=True)

        if not success:
            click.echo(click.style(f"Failed to prepare repository {self.gh_client.repo_name}: {err}", fg="red"))
            return

        if not template_repo:
            success, err = self.gh_client.put_file(
                "README.md",
                "# Benchmark Repository\nGenerated by AI Benchmark Suite.",
                "initial commit",
                "main",
            )
            if not success:
                click.echo(click.style(f"Failed to push initial commit: {err}", fg="red"))

        repo_info = self.gh_client.get_repo_info()
        if not repo_info:
            click.echo(click.style(f"Failed to get repository info for {self.gh_client.repo_name}.", fg="red"))
            return

        # Ensure Actions are enabled (forked repos might have them disabled by default)
        click.echo(f"Enabling GitHub Actions for {self.gh_client.repo_name}...")
        success, err = self.gh_client.enable_actions()
        if not success:
            click.echo(click.style(f"Warning: Failed to enable GitHub Actions: {err}", fg="yellow"))

        # Ensure Issues are enabled
        click.echo(f"Enabling GitHub Issues for {self.gh_client.repo_name}...")
        success, err = self.gh_client.enable_issues()

        if not success:
            click.echo(click.style(f"Warning: Failed to enable GitHub Issues: {err}", fg="yellow"))

        default_branch = repo_info.get("defaultBranchRef", {}).get("name") or "main"
        target_branch = branch or default_branch

        # 1. Collect all workflow files
        workflow_files = {}
        if os.path.isdir(workflow_dir):
            contents_dir = os.path.join(workflow_dir, "contents")
            if os.path.isdir(contents_dir):
                for root, _, filenames in os.walk(contents_dir):
                    for filename in filenames:
                        local_file_path = os.path.join(root, filename)
                        rel_path = os.path.relpath(local_file_path, contents_dir)
                        workflow_files[rel_path] = local_file_path
            else:
                for filename in os.listdir(workflow_dir):
                    if filename.endswith(".yml") or filename.endswith(".yaml"):
                        workflow_path = os.path.join(workflow_dir, filename)
                        workflow_files[f".github/workflows/{filename}"] = workflow_path

        scenario_files = required_files or {}
        for repo_path in scenario_files:
            if repo_path in workflow_files:
                raise ValueError(f"Conflict detected: File '{repo_path}' is defined by both the workflow and the scenario.")

        # 2. Batch sync workflows to the DEFAULT branch
        try:
            # We want to clear ALL existing workflows in the folder and replace them with the new set.
            # This is more efficient than deleting them one by one and avoids orphaned workflows.
            deletions = [".github/workflows/"]

            additions = {}
            for repo_path, content_or_path in workflow_files.items():
                additions[repo_path] = self._get_content(repo_path, content_or_path, substitution_map)

            if additions or deletions:
                click.echo(f"Batch syncing workflows to {default_branch} (clearing .github/workflows/)...")
                success, err = self.gh_client.batch_sync(additions, deletions, "provision workflows", default_branch)
                if not success:
                    click.echo(click.style(f"Failed to batch sync workflows: {err}", fg="red"))
        except Exception as e:
            click.echo(click.style(f"Warning: Failed to clear/sync workflows: {e}", fg="yellow"))

        # 3. Ensure target branch exists (created from default_branch, so it will have workflow files)
        if target_branch != default_branch:
            branch_info = self.gh_client.get_branch_info(target_branch)
            if not branch_info:
                click.echo(f"Creating branch {target_branch} from {default_branch}...")
                success, err = self.gh_client.create_branch(target_branch, default_branch)
                if not success:
                    click.echo(click.style(f"Failed to create branch {target_branch}: {err}", fg="red"))
                    return

        # 4. Sync scenario-specific files to the TARGET branch
        if scenario_files:
            additions = {}
            for repo_path, content_or_path in scenario_files.items():
                additions[repo_path] = self._get_content(repo_path, content_or_path, substitution_map)

            click.echo(f"Batch syncing scenario files to {target_branch}...")
            success, err = self.gh_client.batch_sync(additions, [], "provision scenario files", target_branch)
            if not success:
                click.echo(click.style(f"Failed to batch sync scenario files: {err}", fg="red"))

        # 5. Set Repository Secrets
        if secrets:
            for name, value in secrets.items():
                if value:
                    click.echo(f"Setting repository secret '{name}'...")
                    success, err = self.gh_client.set_secret(name, value)
                    if not success:
                        click.echo(click.style(f"Failed to set secret {name}: {err}", fg="red"))

        # 6. Set Repository Variables
        if variables:
            for name, value in variables.items():
                if value:
                    click.echo(f"Setting repository variable '{name}'...")
                    success, err = self.gh_client.set_variable(name, value)
                    if not success:
                        click.echo(click.style(f"Failed to set variable {name}: {err}", fg="red"))

    def _get_content(self, repo_path, content_or_path, substitution_map):
        """Reads and optionally patches file content."""
        content = content_or_path
        is_binary = False
        if isinstance(content_or_path, str) and os.path.exists(content_or_path):
            with open(content_or_path, "rb") as f:
                content = f.read()
                try:
                    content = content.decode("utf-8")
                    if substitution_map and (repo_path.endswith(".yml") or repo_path.endswith(".yaml")):
                        content = self._patch_yaml(content, substitution_map)
                except UnicodeDecodeError:
                    is_binary = True

        if is_binary:
            return content.decode("latin-1")
        return content

    def _patch_yaml(self, content: str, substitution_map: dict) -> str:
        """Replaces official action references with adversarial forks/tags."""
        import re

        patched_content = content
        for original, replacement in substitution_map.items():
            pattern = rf"uses:\s*['\"]?{re.escape(original)}['\"]?"
            replacement_str = f"uses: {replacement}"
            if re.search(pattern, patched_content):
                click.echo(click.style(f"  PATCHED: Swapping '{original}' -> '{replacement}'", fg="cyan"))
                patched_content = re.sub(pattern, replacement_str, patched_content)

        return patched_content

    def teardown(self):
        """Deletes the entire repository."""
        click.echo(f"Deleting repository {self.gh_client.repo_name}...")
        success, err = self.gh_client.delete_repo()
        if not success:
            click.echo(click.style(f"Failed to delete repository {self.gh_client.repo_name}: {err}", fg="red"))
        else:
            click.echo(f"Successfully deleted repository {self.gh_client.repo_name}.")
